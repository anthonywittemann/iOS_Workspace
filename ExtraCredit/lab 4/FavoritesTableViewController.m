#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.model numberOfFavorites];
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"FavoritesTableCell" forIndexPath:indexPath];
    
    //@QuestionTableCell might not be right ^^^
    
    // Configure the cell...
    NSDictionary *flashcard = [self.model favoriteAtIndex:indexPath.row];
    
    cell.textLabel.text = [flashcard objectForKey: kQuestionKey];
    cell.detailTextLabel.text = [flashcard objectForKey: kAnswerKey];
    
    return cell;
}


